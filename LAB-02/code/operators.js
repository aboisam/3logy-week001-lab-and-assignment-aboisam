// Arithmetic Operations
// Declare two numbers
// Calculate sum
// Calculate product
// Calculate remainder (modulo)
// Log results

let num1 = 50;
let num2 = 20;

let sum = num1 +num2;

let product = num1 * num2; 

let remainder = num1 % num2;

console.log("The Sum of:", sum);
console.log("The Product of:", product);
console.log("The Remainder of:", remainder);

// Comparison Operations
// Compare if two numbers are equal
// Compare if one number is greater than another
// Log results

let isEqual = num1 === num2;
let isGreater = num1 > num2;
console.log("Are the numbers equal?", isEqual);
console.log("Is num1 greater than num2?", isGreater);



// Logical Operations
// Use AND (&&) operator
// Use OR (||) operator
// Log results

let bothTrue = num1 > 5 && num2 < 50;
let eitherTrue = num1 > 5 || num2 > 20;  
console.log("Are both conditions true?", bothTrue);
console.log("Is at least one condition true?", eitherTrue);  